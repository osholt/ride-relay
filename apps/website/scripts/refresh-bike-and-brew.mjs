import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

const MAP_ID = "1N4Oey1CiDFqn2vJuqrgnT4oQhTvyxqU";
const GOOGLE_MAP_URL = `https://www.google.com/maps/d/viewer?mid=${MAP_ID}`;
const KML_URL = `https://www.google.com/maps/d/kml?mid=${MAP_ID}&forcekml=1`;
const CURRENT_MAP_URL = "https://ukbikercafes.co.uk/bike-and-brew-map/";
const OUTPUT_URL = new URL("../bike-and-brew-places.mjs", import.meta.url);
const MAX_MATCH_DISTANCE_METRES = 200;

const [kml, currentMapHtml] = await Promise.all([
  fetchText(KML_URL),
  fetchText(CURRENT_MAP_URL),
]);
const documentName = cleanXml(extractTag(kml, "Document>\\s*<name"));
if (!documentName.includes("Bike and Brew Map 2026")) {
  throw new Error(`Unexpected Google My Maps document: ${documentName || "unnamed"}.`);
}
if (!currentMapHtml.includes("Bike and Brew Passport Event 2026 Map")) {
  throw new Error("The current-event verification map could not be identified.");
}

const exportedPlaces = [...kml.matchAll(/<Placemark>(.*?)<\/Placemark>/gs)].map(
  ([, placemark], index) => parsePlacemark(placemark, index),
);
const activeMarkers = parseCurrentMarkers(currentMapHtml);
if (exportedPlaces.length < 250 || activeMarkers.length < 250) {
  throw new Error(
    `Catalogue is unexpectedly small (${exportedPlaces.length} exported, ${activeMarkers.length} current).`,
  );
}

const withdrawn = [];
const usedMarkerIds = new Set();
const places = [];
for (const exported of exportedPlaces) {
  const match = activeMarkers
    .filter((marker) => !usedMarkerIds.has(marker.sourceId))
    .map((marker) => ({
      marker,
      distance: coordinateDistance(exported, marker),
    }))
    .sort((left, right) => left.distance - right.distance)[0];
  if (!match || match.distance > MAX_MATCH_DISTANCE_METRES) {
    withdrawn.push(exported.name);
    continue;
  }
  usedMarkerIds.add(match.marker.sourceId);
  places.push({
    sourceId: match.marker.sourceId,
    name: match.marker.name || exported.name,
    address: match.marker.address || exported.address,
    latitude: match.marker.latitude,
    longitude: match.marker.longitude,
    sourceUrl: match.marker.sourceUrl,
    googleMapUrl: GOOGLE_MAP_URL,
    source: "Bike + Brew Passport 2026",
  });
}

const unmatchedActive = activeMarkers.filter(
  (marker) => !usedMarkerIds.has(marker.sourceId),
);
if (unmatchedActive.length > 0) {
  throw new Error(
    `Current venues missing from the authorised export: ${unmatchedActive
      .map((place) => place.name)
      .join(", ")}.`,
  );
}

const uniqueCoordinates = new Set();
for (const place of places) {
  if (!place.name || !place.address || !place.sourceUrl) {
    throw new Error(`Venue ${place.sourceId} is missing a name, address, or source URL.`);
  }
  if (
    !Number.isFinite(place.latitude) ||
    !Number.isFinite(place.longitude) ||
    place.latitude < 49 ||
    place.latitude > 61 ||
    place.longitude < -9 ||
    place.longitude > 3
  ) {
    throw new Error(`${place.name} has an invalid UK map location.`);
  }
  const coordinateKey = `${place.latitude},${place.longitude}`;
  if (uniqueCoordinates.has(coordinateKey)) {
    throw new Error(`${place.name} duplicates another venue's map location.`);
  }
  uniqueCoordinates.add(coordinateKey);
}

places.sort((left, right) => left.name.localeCompare(right.name));
const checkedAt = new Date().toISOString().slice(0, 10);
const output = `// Generated by scripts/refresh-bike-and-brew.mjs from an authorised public export.
// Google My Maps source: ${GOOGLE_MAP_URL}
// Checked: ${checkedAt}. Exported points are retained only while also present on
// the current Bike + Brew map; withdrawn or stale points are excluded.

export const BIKE_AND_BREW_PLACES = Object.freeze(${JSON.stringify(places, null, 2)});
`;

await writeFile(OUTPUT_URL, output);
console.log(
  `Wrote ${places.length} current venues to ${fileURLToPath(OUTPUT_URL)}; ` +
    `${withdrawn.length} stale or withdrawn export entries excluded` +
    `${withdrawn.length ? ` (${withdrawn.join(", ")})` : ""}.`,
);

function parsePlacemark(placemark, index) {
  const name = cleanXml(extractTag(placemark, "name"));
  const address = cleanXml(
    placemark.match(
      /<Data name="Venue Address:">\s*<value>(.*?)<\/value>\s*<\/Data>/s,
    )?.[1],
  );
  const coordinateText = placemark.match(/<coordinates>\s*([^<]+)<\/coordinates>/)?.[1];
  const [longitude, latitude] = String(coordinateText)
    .trim()
    .split(",")
    .map(Number);
  if (!name || !Number.isFinite(longitude) || !Number.isFinite(latitude)) {
    throw new Error(`Invalid Google My Maps placemark at index ${index}.`);
  }
  return { name, address, latitude, longitude };
}

function parseCurrentMarkers(html) {
  const markerJson = html.match(/\.setMarkers\((\[.*?\])\)\s*(?:;|\n)/s)?.[1];
  if (!markerJson) throw new Error("Could not find the current Bike + Brew markers.");
  return JSON.parse(markerJson).map((marker) => {
    const content = decodeHtml(marker.content || "");
    const plainText = content.replace(/<[^>]*>/g, " ").replace(/\s+/g, " ");
    if (/permanently\s+closed|closed\s+permanently/i.test(plainText)) {
      throw new Error(`Current map contains a closed listing: ${plainText.slice(0, 120)}.`);
    }
    return {
      sourceId: Number(marker.entity_id),
      name: cleanText(content.match(/data-entity-title="([^"]+)"/)?.[1]),
      address: cleanText(
        content.match(/class="drts-location-address[^"]*"[^>]*>([^<]+)</)?.[1],
      ),
      latitude: Number(marker.lat),
      longitude: Number(marker.lng),
      sourceUrl: cleanText(
        content.match(
          /href="(https:\/\/ukbikercafes\.co\.uk\/directory-cafes\/listing\/[^"]+)"/,
        )?.[1],
      ),
    };
  });
}

async function fetchText(url) {
  const response = await fetch(url, {
    headers: { "User-Agent": "TailEndCharlie authorised catalogue updater" },
  });
  if (!response.ok) throw new Error(`${url} returned ${response.status}.`);
  return response.text();
}

function extractTag(xml, tagPattern) {
  return xml.match(new RegExp(`<${tagPattern}>(.*?)<\\/[^>]+>`, "s"))?.[1] || "";
}

function cleanXml(value) {
  return decodeHtml(
    String(value || "")
      .replace(/^<!\[CDATA\[/, "")
      .replace(/\]\]>$/, ""),
  );
}

function decodeHtml(value) {
  return cleanText(
    String(value || "")
      .replace(/&#(\d+);/g, (_match, code) => String.fromCodePoint(Number(code)))
      .replace(/&#x([0-9a-f]+);/gi, (_match, code) =>
        String.fromCodePoint(Number.parseInt(code, 16)),
      )
      .replaceAll("&amp;", "&")
      .replaceAll("&quot;", '"')
      .replaceAll("&#039;", "'")
      .replaceAll("&lt;", "<")
      .replaceAll("&gt;", ">"),
  );
}

function cleanText(value) {
  return String(value || "").replace(/\s+/g, " ").trim();
}

function coordinateDistance(first, second) {
  const radians = Math.PI / 180;
  const latitude1 = first.latitude * radians;
  const latitude2 = second.latitude * radians;
  const latitudeDelta = latitude2 - latitude1;
  const longitudeDelta = (second.longitude - first.longitude) * radians;
  const haversine =
    Math.sin(latitudeDelta / 2) ** 2 +
    Math.cos(latitude1) *
      Math.cos(latitude2) *
      Math.sin(longitudeDelta / 2) ** 2;
  return 6371000 * 2 * Math.atan2(Math.sqrt(haversine), Math.sqrt(1 - haversine));
}
